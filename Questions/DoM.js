// DOM (Document Object Model)
// DOM stands for Document Object Model.
// It is a tree-like structure created by the browser 
// that represents the HTML document as objects.

// How it works:
// The browser parses HTML and converts it into nodes.
// Each HTML tag is treated as a node in the DOM tree.

// Why we use DOM:
// DOM allows JavaScript to access, modify, add, and delete elements dynamically.

// Example:
// document.getElementById("title").innerText = "Hello World";
// document.querySelector()
// document.getElementsByClassName()

// “DOM is a tree-like structure created by the browser that represents an HTML document as objects, allowing JavaScript to dynamically access and manipulate elements.”

