// Arrow Function is a shorter syntax to write function expressions in JavaScript.
// It makes code more concise and readable.

// Key Features:

// 1. Short and concise syntax
// 2. Does not have its own "this" (uses lexical this)
// 3. Does not have its own "arguments" object
// 4. Supports implicit return
// 5. Cannot be used as a constructor (cannot use 'new')

